/**
 * @author japarejo
 */
@Configuration
@EnableWebSecurity
public class SecurityConfiguration extends WebSecurityConfigurerAdapter {

	@Autowired
	DataSource dataSource;
	
	@Override
	protected void configure(HttpSecurity http) throws Exception {
		http.csrf().ignoringAntMatchers("/lobby/**").and().authorizeRequests().antMatchers("/lobby/**").permitAll();
		http.authorizeRequests()
				.antMatchers("/resources/**","/webjars/**","/h2-console/**").permitAll()
				.antMatchers(HttpMethod.GET, "/","/oups").permitAll()
				.antMatchers("/users/new").permitAll()
				.antMatchers("/error").permitAll()
				.antMatchers("/chat/**").authenticated() //sockets
				.antMatchers("/lobbymatch/**").authenticated() //sockets
				.antMatchers("/dumpDb").authenticated()
				.antMatchers("/game/**").authenticated() //sockets
				.antMatchers("/dice/**").authenticated() //sockets
				.antMatchers("/lobby/**").authenticated()
				.antMatchers("/estadisticas/**").authenticated()
				.antMatchers(HttpMethod.POST, "/lobby/invite/**").permitAll()
				.antMatchers("/users/**/edit").hasAnyAuthority("admin")
				.antMatchers("/users/**").hasAnyAuthority("admin","user")
				.antMatchers("/admin/**").hasAnyAuthority("admin")
				.antMatchers("/game/**").permitAll()
				.antMatchers("/owners/**").hasAnyAuthority("user","admin")				
				.antMatchers("/vets/**").authenticated()
				.anyRequest().denyAll()
				.and()
				 	.formLogin()
				 	.loginPage("/login").permitAll()
				.and()
					.logout().permitAll()
						.logoutSuccessUrl("/"); 
                // Configuraci칩n para que funcione la consola de administraci칩n 
                // de la BD H2 (deshabilitar las cabeceras de protecci칩n contra
                // ataques de tipo csrf y habilitar los framesets si su contenido
                // se sirve desde esta misma p치gina.
                http.csrf().ignoringAntMatchers("/h2-console/**");
                http.headers().frameOptions().sameOrigin();
				http.sessionManagement().maximumSessions(1).sessionRegistry(sessionRegistry());
	}

	@Override
	public void configure(AuthenticationManagerBuilder auth) throws Exception {
		auth.jdbcAuthentication()
	      .dataSource(dataSource)
	      .usersByUsernameQuery(
	       "select username,password,enabled "
	        + "from users "
	        + "where username = ?")
	      .authoritiesByUsernameQuery(
	       "select username, authority "
	        + "from authorities "
	        + "where username = ?")	      	      
	      .passwordEncoder(passwordEncoder());	
	}
	
	@Bean
	public PasswordEncoder passwordEncoder() {	    
		PasswordEncoder encoder =  NoOpPasswordEncoder.getInstance();
	    return encoder;
	}

	@Bean
    public SessionRegistry sessionRegistry() {
        return new SessionRegistryImpl();
    }

    @Bean
    public ServletListenerRegistrationBean<HttpSessionEventPublisher> httpSessionEventPublisher() {
        return new ServletListenerRegistrationBean<HttpSessionEventPublisher>(new HttpSessionEventPublisher());
    }
	
}


